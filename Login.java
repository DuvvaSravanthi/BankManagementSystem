    public void load() {                   
        try {
            // Load the Oracle JDBC driver
            Class.forName("oracle.jdbc.driver.OracleDriver");
            
            // Establish connection to the Oracle database
            con1 = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "root");
            
            // Prepare the SQL statement
            insert = con1.prepareStatement("SELECT * FROM user_table WHERE username=? and password=? ");
            
            // Set the parameters for the SQL statement
            insert.setString(1, txtuser.getText());
            insert.setString(2, new String(txtpass.getPassword()));
            
            // Execute the query
            rs1 = insert.executeQuery();
            
            // Check if user exists
            if(rs1.next()) {
                // If user exists, open the main menu window
                mainmenu c = new mainmenu();
                this.dispose(); // Close the login window
                c.setVisible(true);
            } else {
                // If user doesn't exist, show error message
                JOptionPane.showMessageDialog(null, "Username and password do not match");
                txtuser.setText("");
                txtpass.setText("");
                txtuser.requestFocus();
            }               
                       
        } catch (Exception e) {
            // Handle any exceptions
            System.out.println("Failed " + e);
        }             
    }

